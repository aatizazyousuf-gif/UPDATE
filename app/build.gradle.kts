android {
  namespace = "com.example"
  compileSdk { version = release(36) { minorApiLevel = 1 } }

  defaultConfig {
    applicationId = "com.aistudio.gasmonitor.wtpnbq"
    minSdk = 24
    targetSdk = 36
    versionCode = 1
    versionName = "1.0"

    testInstrumentationRunner = "androidx.test.runner.AndroidJUnitRunner"
  }

  signingConfigs {
    // Only registers a real "release" signing config if a keystore actually exists at the
    // configured path. This avoids a hard Gradle failure when no paid signing certificate /
    // keystore has been set up yet - you can still build and run everything for free using
    // the debug keystore below, and add real release signing later when you're ready to
    // publish to the Play Store.
    val keystorePath = System.getenv("KEYSTORE_PATH") ?: "${rootDir}/my-upload-key.jks"
    if (file(keystorePath).exists()) {
      create("release") {
        storeFile = file(keystorePath)
        storePassword = System.getenv("STORE_PASSWORD")
        keyAlias = "upload"
        keyPassword = System.getenv("KEY_PASSWORD")
      }
    }
    // debug.keystore is gitignored and NOT included in this project by default, so we only
    // register this custom debug signing config if that file actually exists. Otherwise the
    // debug build type below is left unassigned, which makes Gradle/Android Studio fall back
    // to its own auto-generated debug keystore (~/.android/debug.keystore) - completely free
    // and requires zero manual setup to just hit Run.
    if (file("${rootDir}/debug.keystore").exists()) {
      create("debugConfig") {
        storeFile = file("${rootDir}/debug.keystore")
        storePassword = "android"
        keyAlias = "androiddebugkey"
        keyPassword = "android"
      }
    }
  }

  buildTypes {
    release {
      isCrunchPngs = false
      isMinifyEnabled = false
      proguardFiles(getDefaultProguardFile("proguard-android-optimize.txt"), "proguard-rules.pro")
      // Falls back to the default auto-generated debug keystore if no real release keystore
      // has been created yet, so `assembleRelease` never fails with a missing-keystore error.
      signingConfig = signingConfigs.findByName("release") ?: signingConfigs.findByName("debugConfig")
    }
    debug {
      // Uses the custom debugConfig only if debug.keystore was actually found above;
      // otherwise Android's default auto-generated debug keystore is used automatically.
      signingConfigs.findByName("debugConfig")?.let { signingConfig = it }
    }
  }
  compileOptions {
    sourceCompatibility = JavaVersion.VERSION_11
    targetCompatibility = JavaVersion.VERSION_11
  }
  buildFeatures {
    compose = true
    buildConfig = true
  }
  testOptions { unitTests { isIncludeAndroidResources = true } }
}

